package ks47team03.user.dto;

public class Kiosk {
	private String partnerName;
	private String installKioskPlace;
	private String kioskInstalleddatetime;
	public String getPartnerName() {
		return partnerName;
	}
	public void setPartnerName(String partnerName) {
		this.partnerName = partnerName;
	}
	public String getInstallKioskPlace() {
		return installKioskPlace;
	}
	public void setInstallKioskPlace(String installKioskPlace) {
		this.installKioskPlace = installKioskPlace;
	}
	public String getKioskInstalleddatetime() {
		return kioskInstalleddatetime;
	}
	public void setKioskInstalleddatetime(String kioskInstalleddatetime) {
		this.kioskInstalleddatetime = kioskInstalleddatetime;
	}
}
